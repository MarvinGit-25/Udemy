const raio = 5.6
const area = Math.PI * Math.pow(raio, 2) // Math é um objeto, PI é sua constante, Math.pow é uma função que calcula a potência.

console.log(area.toFixed(2)) // toFixed controla a quantidades de casa decimais.
console.log(typeof Math) // Math é um objeto, e typeof indica o tipo de operador.

console.log(Math.pow(5, 2)) // retorna a base elevada ao expoente power, ou seja, baseexpoente.

console.log(Math.sqrt(36)) // A função Math.sqrt() retorna a raiz quadrada de um número não negativo

console.log(Math.abs(-10)) // retorna o valor absoluto de um número


