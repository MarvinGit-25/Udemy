const a = 7
let b = 3

b += a // b = b + a // atribuição de soma.
console.log(b)

b -= 4 // b = b - 4 // atribuição de subtração.
console.log(b)

b *= 2 // b = b * 2 atribuição de multiplicação.
console.log(b)

b /= 2 // b = b / 2 atribuição de divisão.
console.log(b)

b %= 2 // b = b % 2 atribuição de resto da divisão.
console.log(b)
